package clasesJava;

public class Calculadora {
    final float PI = 3.14f;

    public float sumar(float a, float b){
        float resultado = a+b;
        return  resultado;
    }
    public float restar(float a, float b){
        float resultado = a-b;
        return  resultado;
    }
    public float multiplicar(float a, float b){
        float resultado = a*b;
        return  resultado;
    }
}
